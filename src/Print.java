public class Print {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.print("World");
        System.out.println("!");
        // println - переносит следующее значение на новую строку
        System.out.println("Hello\n\tWorld!");
        // \n - перенос на следюущю строку
        // \t - отсутуп
        // \" - поставить кавычки
        // \\ - слэш
    }
}
